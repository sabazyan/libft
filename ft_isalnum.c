#include "libft.h"

int	ft_isalnum(int c)
{
	int	result;

	result = 1;
	if (c < 48 || (c > 57 && c < 65))
		result = 0;
	else if ((c > 90 && c < 97) || c > 122)
		result = 0;
	return (result);
}
